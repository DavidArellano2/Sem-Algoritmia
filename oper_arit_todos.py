#Algoritmo de las expresiones y operadores aritmeticas con sus variables y tipo de datos

#Entrada de datos
n1=int(input("Dame un numero: "));
n2=int(input("Dame otro numero: "));

#Suma
r=n1+n2;
print("El resultado de la suma es: ",r);

#Resta
r=n1-n2;
print("El resultado de la resta es: ",r);

#Multiplicacion
r=n1*n2;
print("El resultado de la multipliacion es: ",r);

#Division
r=n1/n2;
print("El resultado de la division es: ",r);

#Division entera
r=n1//n2;
print("El resultado de la division entera es: ",r);

#Aplicando residuo
r=n1%n2;
print("El resultado de el residuo de la division es: ",r);

#Potencia
r=n1**n2;
print("El resultado de la potencia es: ",r);
